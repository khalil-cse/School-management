    public boolean isPhoneExist(String phone) {
        try {
            ps = con.prepareStatement("select * from staff where contact = ?");
            ps.setString(1, phone);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                return true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageStaffs.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

