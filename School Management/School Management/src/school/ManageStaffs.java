    private void getStaffDetails(int stid) {
        try {
            ps = con.prepareStatement("select * from staff where id = ?");
            ps.setInt(1, stid);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
               
                fn.setText(rs.getString("firstName"));
                ln.setText(rs.getString("lastName"));
                
                String gender = rs.getString(5);
                if (gender.equals("Male")) {
                    gen.setSelectedIndex(0);
                } else {
                    gen.setSelectedIndex(1);
                }
                fan.setText(rs.getString("fatherName"));
                mon.setText(rs.getString("motherName"));
                co.setText(rs.getString("contact"));
                co.setEditable(false);
                co.setBackground(new Color(204, 204, 204));
                
                add.setText(rs.getString("address"));
                salaryedit.setText(rs.getString("salary"));
               
                btnSave.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnDelete.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Staff id doesn't exist");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

